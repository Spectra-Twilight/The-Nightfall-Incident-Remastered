#include <Spybot/Game.hpp>

using namespace std;
using namespace Spybot;

//============================================================================================================================================================

void Game::draw(sf::RenderTarget& target, sf::RenderStates states) const
{
    switch (state)
    {   //----------------------------------------------------------------------------------------------------------------------------------------------------
        case State::level:
        {
            target.draw(level);
        }
        break;
    }   //----------------------------------------------------------------------------------------------------------------------------------------------------
}

//============================================================================================================================================================

void Game::onMouseButtonPressed(const sf::Event& event)
{
    switch (state)
    {   //----------------------------------------------------------------------------------------------------------------------------------------------------
        case State::level:
        {
            level.onMouseButtonPressed(event);
        }
        break;
    }   //----------------------------------------------------------------------------------------------------------------------------------------------------
}

//============================================================================================================================================================

void Game::onMouseButtonReleased(const sf::Event& event)
{
    switch (state)
    {   //----------------------------------------------------------------------------------------------------------------------------------------------------
        case State::level:
        {
            level.onMouseButtonReleased(event);
        }
        break;
    }   //----------------------------------------------------------------------------------------------------------------------------------------------------
}